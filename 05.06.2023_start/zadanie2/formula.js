export function perimetrPr(a,b){
    return 2*(a+b);
}

export function ploshadPr(a,b){
    return a*b;
}

export function perimetrKrug(r){
    return Math.PI*2*r;
}

export function ploshadKrug(r){
    return Math.PI*r*r;
}

export function perimetrKvadr(a){
    return 4*a;
}

export function ploshadKvadr(a){
    return a*a;
}